export const TabBar: React.FC<TabBarProps> = ({
  items = defaultTabItems,
  className = '',
  iconsOnly = false,
}) => {
  const pathname = usePathname();

  const isActive = (href: string) => {
    if (href === '/dashboard' && pathname === '/') return true;
    return pathname?.startsWith(href);
  };

  return (
    <nav
      className={`
        fixed bottom-0 left-0 right-0
        bg-white border-t border-neutral-200
        safe-area-pb
        ${className}
      `}
      aria-label="Main navigation"
    >
      <div className="max-w-lg mx-auto px-4">
        <ul className="flex items-center justify-around h-16">
          {items.map((item) => {
            const active = isActive(item.href);
            return (
              <li key={item.id}>
                <Link
                  href={item.href}
                  className={`
                    flex flex-col items-center justify-center
                    min-w-[64px] py-2 px-3
                    rounded-lg transition-colors
                    ${active
                      ? 'text-primary-600'
                      : 'text-neutral-500 hover:text-neutral-700 hover:bg-neutral-50'
                    }
                  `}
                  aria-current={active ? 'page' : undefined}
                >
                  <span className="relative">
                    {active ? (item.activeIcon || item.icon) : item.icon}
                    {item.badge !== undefined && item.badge > 0 && (
                      <span className="absolute -top-1 -right-1 flex items-center justify-center min-w-[18px] h-[18px] px-1 text-xs font-bold text-white bg-danger-500 rounded-full">
                        {item.badge > 99 ? '99+' : item.badge}
                      </span>
                    )}
                  </span>
                  {!iconsOnly && (
                    <span className={`text-xs mt-1 font-medium ${active ? 'text-primary-600' : ''}`}>
                      {item.label}
                    </span>
                  )}
                </Link>
              </li>
            );
          })}
        </ul>
      </div>
    </nav>
  );
};

export default TabBar;
